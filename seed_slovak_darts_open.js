function formatPlayerName(value) {
  if (!value.includes(',')) return value;

  const parts = value.split(',');
  const lastName = parts[0].trim();
  const firstName = parts[1].trim();

  return `${firstName} ${lastName}`;
}

function roundLegInfo(round) {
  if (round === 'Halbfinale') return { targetLegs: 7, bestOfLegs: 13 };
  if (round === 'Finale') return { targetLegs: 8, bestOfLegs: 15 };

  return { targetLegs: 6, bestOfLegs: 11 };
}

function parseScore(scoreText) {
  const parts = scoreText.replace(/\s+/g, '').split(':');

  return {
    legs1: Number(parts[0] ?? 0),
    legs2: Number(parts[1] ?? 0),
    legs: `${parts[0]}-${parts[1]}`,
  };
}

function createMatchId(round, order, player1, player2, scoreText) {
  return `slovak-darts-open-2026-${round}-${order}-${player1}-${player2}-${scoreText}`
    .toLowerCase()
    .replace(/[^a-z0-9äöüß]+/gi, '-')
    .replace(/^-+|-+$/g, '');
}

function buildSlovakDartsOpenSeedMatches() {
  return slovakDartsOpenMatches.map((row, index) => {
    const [
      round,
      roundOrder,
      matchOrder,
      player1,
      player2,
      scoreText,
      dateLabel,
      dateIndex,
    ] = row;

    const score = parseScore(scoreText);
    const legInfo = roundLegInfo(round);
    const winner = score.legs1 > score.legs2 ? player1 : player2;

    return {
      id: createMatchId(round, matchOrder, player1, player2, scoreText),
      tournament: 'Slovak Darts Open',
      tournamentName: 'Slovak Darts Open',
      tournamentRuleName: 'slovak_darts_open',
      hasTournamentRule: true,
      round,
      roundName: round,
      roundOrder,
      dateLabel,
      dateIndex,
      matchOrder,
      bracketPosition: matchOrder,
      player1,
      player2,
      homeName: formatPlayerName(player1),
      awayName: formatPlayerName(player2),
      player1DisplayName: formatPlayerName(player1),
      player2DisplayName: formatPlayerName(player2),
      startTime: null,
      sets: null,
      legs: score.legs,
      legs1: score.legs1,
      legs2: score.legs2,
      homeScore: String(score.legs1),
      awayScore: String(score.legs2),
      targetLegs: legInfo.targetLegs,
      bestOfLegs: legInfo.bestOfLegs,
      scoreText,
      status: 'finished',
      isLive: false,
      isScheduled: false,
      isFinished: true,
      winner,
      winnerDisplayName: formatPlayerName(winner),
      globalOrder: index + 1,
    };
  });
}

